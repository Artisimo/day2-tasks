import java.lang.*;
import java.util.*;
public class Main {

    public static void main(String[] args) {
        byte byte1 = 127;
        short short1 = 32000;
        int int1 = 2000000000;
        long long1 = 12123144;
        float float1 = 1.123f;
        double double1 = 1.1234;
        boolean isTrue = true;
        char c = 'c';

        System.out.println("Byte: " + byte1 + "\n" +
                            "Short: " + short1 + "\n" +
                            "Int: " + int1  + "\n" +
                            "Long: " + long1 + "\n" +
                            "Float: " + float1 + "\n" +
                            "Double: " + double1 + "\n" +
                            "Boolean: " + isTrue + "\n" +
                            "Char: " + c);
    }
}
